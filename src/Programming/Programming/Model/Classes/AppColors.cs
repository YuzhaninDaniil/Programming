namespace Programming
{
    /// <summary>
    /// Хранит цветовую палитру.
    /// </summary>
    public static class AppColors
    {
        /// <summary>
        /// Представляет светло-розовый цвет.
        /// </summary>
        public static readonly Color LightPink = Color.LightPink;

        /// <summary>
        /// Представляет белый цвет.
        /// </summary>
        public static readonly Color White = Color.White;

        /// <summary>
        /// Представляет зеленый цвет с определенным RGBA значением.
        /// </summary>
        public static readonly Color Green = Color.FromArgb(127, 127, 255, 127);

        /// <summary>
        /// Представляет красный цвет с определенным RGBA значением.
        /// </summary>
        public static readonly Color Red = Color.FromArgb(127, 255, 127, 127);
    }
}

